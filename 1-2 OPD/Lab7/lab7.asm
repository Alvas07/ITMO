ORG 0x18F
SAVE_TEMP: WORD 0 ; Временная переменная для хранения промежуточного значения

ORG 0x190
TEST1: WORD 0x0000
TEST2: WORD 0x0001
TEST3: WORD 0xFFFF
TEST4: WORD 0xF000
TEST5: WORD 0x0FFF
TEST6: WORD 0xEEFF
TEST7: WORD 0x8EF6
TEST8: WORD 0x3425

ORG 0x1A0
RES1: WORD 0x0000
RES2: WORD 0x0002
RES3: WORD 0xFFFE
RES4: WORD 0xE000
RES5: WORD 0x1FFE
RES6: WORD 0xDDFE
RES7: WORD 0x1DEC
RES8: WORD 0x684A

ORG 0x1B0
RESULT: WORD 0
TEST_COUNT: WORD 8
START_TEST: WORD $TEST1
START_RES: WORD $RES1

ORG 0x3BC
START: 	LD $TEST_COUNT
		BEQ STOP

		LD (START_TEST)+
		ST $SAVE_TEMP
		WORD 0x918F
		LD $SAVE_TEMP
		CMP (START_RES)+
		BNE NEQ1


EQ1: 	LD $RESULT
		ASL
		INC
		ST $RESULT
		JUMP POST_WORK


NEQ1: 	LD $RESULT
		ASL
		ST $RESULT


POST_WORK: 	LD $TEST_COUNT
			DEC
			ST $TEST_COUNT
			JUMP START


STOP: 	HLT